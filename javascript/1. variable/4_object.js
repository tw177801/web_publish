// object : Array(배열 : []), JSON ({})..
let apple = null;
apple = {
    name: 'apple',
    color: 'red', 
    display: '🍎'
};
console.log(apple);
console.log(apple.display);
console.log(apple.name);
console.log(apple.color);

// orange JSON 객체 생성 후 출력
let orange = null;
orange = {
    name: 'orange',
    color: 'orange',
    display: '🍊',
    translation: '🚚'
};




console.log(orange);
